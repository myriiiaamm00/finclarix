from datetime import datetime
from src.risk_scoring import RISK_EMOJI, RISK_COLOR
from src.i18n import t

# fpdf2 is a pure-Python, MIT/LGPL-licensed PDF library (no system binaries,
# no paid tier — free as in both "no cost" and "open source"). It's the
# lightest-weight way to add a "download as PDF" option per the project's
# business plan, without pulling in a heavyweight HTML→PDF toolchain
# (e.g. WeasyPrint, which needs system-level Cairo/Pango libraries).
from fpdf import FPDF

# Maps clause breakdown dict keys → i18n label keys (same as app.py)
_BREAKDOWN_LABEL_KEYS = (
    ("explanation", "label_explanation"),
    ("why_it_matters", "label_why"),
    ("financial_impact", "label_financial_impact"),
    ("suggested_action", "label_suggested_action"),
)

_LEVEL_METRIC_KEY = {
    "High": "metric_high",
    "Medium": "metric_medium",
    "Low": "metric_low",
    "Informational": "metric_informational",
}


def generate_report(results: list[dict], source_name: str = "contract", exposure_items: list[dict] | None = None) -> str:
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M")

    by_level: dict[str, list[dict]] = {
        "High": [], "Medium": [], "Low": [], "Informational": []
    }
    for r in results:
        by_level[r["risk"]].append(r)

    lines = [
        "# FinClariX Contract Analysis Report",
        "",
        f"**Generated:** {timestamp}",
        f"**Source:** {source_name}",
        "",
        "---",
        "",
        "## Summary",
        "",
        "| Risk Level | Clauses |",
        "|---|:---:|",
    ]
    for level in ("High", "Medium", "Low", "Informational"):
        emoji = RISK_EMOJI[level]
        lines.append(f"| {emoji} {level} | {len(by_level[level])} |")

    lines += ["", "---", ""]

    if exposure_items:
        lines += [
            "## Financial Exposure Summary",
            "",
            "_Figures below are computed deterministically from amounts and time "
            "periods stated directly in the contract — not estimated by AI._",
            "",
        ]
        for item in exposure_items:
            lines.append(f"- {item['text']}")
        lines += ["", "---", ""]

    for level in ("High", "Medium", "Low", "Informational"):
        group = by_level[level]
        if not group:
            continue
        emoji = RISK_EMOJI[level]
        lines.append(f"## {emoji} {level} Risk Clauses")
        lines.append("")
        for i, clause in enumerate(group, 1):
            preview = clause["text"][:300] + ("..." if len(clause["text"]) > 300 else "")
            lines += [
                f"### Clause {i}",
                "",
                f"> {preview}",
                "",
            ]
            if clause.get("keywords"):
                kw_list = "`, `".join(clause["keywords"])
                lines.append(f"**Flagged terms:** `{kw_list}`")
                lines.append("")
            breakdown = clause.get("breakdown")
            if breakdown:
                for key, label_key in _BREAKDOWN_LABEL_KEYS:
                    value = breakdown.get(key)
                    if value:
                        label = t(label_key, "English")
                        lines.append(f"**{label}:** {value}")
                        lines.append("")
        lines += ["---", ""]

    lines += [
        "_Report generated by [FinClariX](https://github.com/myriiiaamm/finclarix) -- "
        "Finance that sticks, minus the tricks._",
    ]
    return "\n".join(lines)


